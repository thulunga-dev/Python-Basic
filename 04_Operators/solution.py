
"""
Challenge Solution: Python Operators
"""

print("=" * 50)
print("          PYTHON OPERATORS")
print("=" * 50)

# ==========================================
# Create Two Numbers
# ==========================================

num1 = 20
num2 = 5

print(f"num1 = {num1}")
print(f"num2 = {num2}")

# ==========================================
# Arithmetic Operators
# ==========================================

print("\n1. Arithmetic Operators")
print("-" * 30)

print("Addition        :", num1 + num2)
print("Subtraction     :", num1 - num2)
print("Multiplication  :", num1 * num2)
print("Division        :", num1 / num2)
print("Floor Division  :", num1 // num2)
print("Modulus         :", num1 % num2)
print("Exponentiation  :", num1 ** num2)

# ==========================================
# Assignment Operators
# ==========================================

print("\n2. Assignment Operators")
print("-" * 30)

result = 10
print("Initial Value :", result)

result += 5
print("After += 5    :", result)

result -= 3
print("After -= 3    :", result)

result *= 2
print("After *= 2    :", result)

result /= 4
print("After /= 4    :", result)

result //= 2
print("After //= 2   :", result)

result %= 3
print("After %= 3    :", result)

result **= 2
print("After **= 2   :", result)

# ==========================================
# Comparison Operators
# ==========================================

print("\n3. Comparison Operators")
print("-" * 30)

print("num1 == num2 :", num1 == num2)
print("num1 != num2 :", num1 != num2)
print("num1 > num2  :", num1 > num2)
print("num1 < num2  :", num1 < num2)
print("num1 >= num2 :", num1 >= num2)
print("num1 <= num2 :", num1 <= num2)

# ==========================================
# Logical Operators
# ==========================================

print("\n4. Logical Operators")
